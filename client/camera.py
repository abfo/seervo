class ArduCAM:

    def __init__(self, spi, cs_pin, i2c, resolution=RES_320x240):
        self._spi = spi
        self._cs = cs_pin
        self._i2c = i2c
        self._cs.value(1)
        self._resolution = resolution

    # ---- SPI helpers ----

    def _spi_write(self, reg, val):
        self._cs.value(0)
        self._spi.write(bytes([reg | 0x80, val]))
        self._cs.value(1)

    def _spi_read(self, reg):
        self._cs.value(0)
        self._spi.write(bytes([reg & 0x7F]))
        result = self._spi.read(1)
        self._cs.value(1)
        return result[0]

    # ---- I2C helpers ----

    def _sensor_write(self, reg, val):
        self._i2c.writeto(OV2640_ADDR, bytes([reg, val]))

    def _sensor_read(self, reg):
        self._i2c.writeto(OV2640_ADDR, bytes([reg]))
        return self._i2c.readfrom(OV2640_ADDR, 1)[0]

    def _write_regs(self, reg_list):
        for reg, val in reg_list:
            self._sensor_write(reg, val)
            time.sleep_ms(1)

    # ---- Public interface ----

    def test_spi(self):
        """Write/read test register. Returns True if SPI works."""
        self._spi_write(ARDUCHIP_TEST, 0x55)
        return self._spi_read(ARDUCHIP_TEST) == 0x55

    def test_i2c(self):
        """Scan for OV2640 on I2C. Returns True if found."""
        return OV2640_ADDR in self._i2c.scan()

    def init(self, resolution=None):
        """Initialize the OV2640 sensor for JPEG capture."""
        if resolution is not None:
            self._resolution = resolution

        # Reset sensor
        self._sensor_write(0xFF, 0x01)
        self._sensor_write(0x12, 0x80)
        time.sleep_ms(100)

        # Load register tables
        self._write_regs(OV2640_JPEG_INIT)
        self._write_regs(OV2640_YUV422)
        self._write_regs(OV2640_JPEG)

        # Set JPEG output
        self._sensor_write(0xFF, 0x01)
        self._sensor_write(0x15, 0x00)

        # Set resolution
        self.set_resolution(self._resolution)
        time.sleep_ms(100)

    def set_resolution(self, resolution):
        """Change JPEG output resolution."""
        self._resolution = resolution
        regs = OV2640_RESOLUTION.get(resolution)
        if regs:
            self._write_regs(regs)

    def capture(self):
        """Capture a single JPEG frame. Returns bytes."""
        # Clear and start
        self._spi_write(ARDUCHIP_CAPTURE_CTL, FIFO_CLEAR_MASK)
        self._spi_write(ARDUCHIP_CAPTURE_CTL, FIFO_START_MASK)

        # Wait for capture done
        start = time.ticks_ms()
        while not (self._spi_read(ARDUCHIP_STATUS) & CAPTURE_DONE_MASK):
            if time.ticks_diff(time.ticks_ms(), start) > 5000:
                raise RuntimeError('Capture timeout')
            time.sleep_ms(10)

        # Read FIFO size
        size1 = self._spi_read(ARDUCHIP_FIFO_SIZE1)
        size2 = self._spi_read(ARDUCHIP_FIFO_SIZE2)
        size3 = self._spi_read(ARDUCHIP_FIFO_SIZE3) & 0x7F
        length = size1 | (size2 << 8) | (size3 << 16)

        if length == 0:
            raise RuntimeError('FIFO empty')
        if length > 500000:
            raise RuntimeError(f'FIFO size too large: {length}')

        # Burst-read the JPEG data
        self._cs.value(0)
        self._spi.write(bytes([ARDUCHIP_FIFO_BURST]))
        data = self._spi.read(length)
        self._cs.value(1)

        return data

    def capture_to_file(self, filename='photo.jpg'):
        """Capture a JPEG and save it to a file. Returns file size."""
        data = self.capture()
        with open(filename, 'wb') as f:
            f.write(data)
        return len(data)
